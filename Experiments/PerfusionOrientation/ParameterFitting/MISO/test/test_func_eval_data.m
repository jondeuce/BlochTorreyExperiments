function func_eval_data = test_func_eval_data(type)

if nargin < 1; type = 'miso'; end

switch upper(type)
    case 'FMINSEARCH'
        func_eval_data = get_fminsearch_data();
    otherwise
        func_eval_data = get_miso_data();
end

[~, ix, ~] = unique(func_eval_data(:, 1:3), 'rows');
func_eval_data = func_eval_data(ix, :);
[~, iy] = sort(func_eval_data(:, 4), 'descend');
func_eval_data = func_eval_data(iy, :);

func_eval_data = struct(...
    'iBVF', func_eval_data(:,1), ...
    'aBVF', func_eval_data(:,2), ...
    'CA',   func_eval_data(:,3), ...
    'AICc', func_eval_data(:,4)  ...
    );

func_eval_data.xdata = [func_eval_data.iBVF, func_eval_data.aBVF, func_eval_data.CA];
func_eval_data.ydata = func_eval_data.AICc;
func_eval_data.xbest = func_eval_data.xdata(end, :);
func_eval_data.ybest = func_eval_data.ydata(end);
func_eval_data.iBVFbest = func_eval_data.iBVF(end);
func_eval_data.aBVFbest = func_eval_data.aBVF(end);
func_eval_data.CAbest = func_eval_data.CA(end);

end

